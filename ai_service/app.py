from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
from models.fracture_detector import router as fracture_router

app = FastAPI()

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

app.include_router(fracture_router, prefix="/fracture", tags=["fracture"])

@app.get("/health")
def health_check():
    return {"status": "ok"}

if __name__ == "__main__":
    import uvicorn
    uvicorn.run(app, host="0.0.0.0", port=5000)
